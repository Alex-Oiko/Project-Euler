package maximum.path;

public class Pair {
	
	private int depth;
	private int value;
	
	public Pair(int depth,int value){
		this.depth=depth;
		this.value=value;
	}

	public int getDepth() {
		return depth;
	}

	public void setDepth(int depth) {
		this.depth = depth;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	

}
